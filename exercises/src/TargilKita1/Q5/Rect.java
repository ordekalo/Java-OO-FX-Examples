package TargilKita1.Q5;

public class Rect {
    private int width;
    private int len;

    public int getWidth() {
        return width;
    }

    public void setWidth(int width) {
        this.width = width;
    }

    public void setLen(int len) {
        this.len = len;
    }

    public int area() {
        return width * len;
    }
}
